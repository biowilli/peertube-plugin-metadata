function setParser(analysedJSON, formData) {
  const container = {};
  for (const field of formData) {
    var mappingname = field.mappingname;
    if (!mappingname) {
      continue;
    }

    var path = mappingname.split(".");
    var currentlevel = analysedJSON[path.shift()];
    if (!currentlevel) {
      continue;
    }
    var success = true;
    for (const p of path) {
      if (Array.isArray(currentlevel)) {
        try {
          currentlevel = currentlevel[p];

          if (currentlevel === undefined) {
            success = false;
            break;
          }
          continue;
        } catch (error) {
          success = false;
          break;
        }
      }

      currentlevel = currentlevel[p];
      if (currentlevel === undefined) {
        success = false;
        break;
      }
    }

    if (success) {
      container[mappingname] = currentlevel;
      console.log("current value: ", currentlevel);
    } else {
      console.log("value does not exist");
    }
  }
  return container;
}

function getParser(analysedJSON) {
  const container = {};
  for (const key in analysedJSON) {
    const keys = key.split(".");
    let currentlevel = container;

    for (let i = 0; i < keys.length; i++) {
      const currentKey = keys[i];
      if (i === keys.length - 1) {
        currentlevel[currentKey] = analysedJSON[key];
      } else {
        currentlevel[currentKey] = currentlevel[currentKey] || {};
        currentlevel = currentlevel[currentKey];
      }
    }
  }

  return container;
}

